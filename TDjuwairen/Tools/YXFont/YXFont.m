#import "YXFont.h"

@implementation YXFont
+ (UIFont *)lightFontSize:(CGFloat)size
{
    return [UIFont fontWithName:@"STHeitiSC-Light" size:size];
}

+ (UIFont *)mediumFontSize:(CGFloat)size
{
    return [UIFont fontWithName:@"STHeitiSC-Medium" size:size];
}
@end
